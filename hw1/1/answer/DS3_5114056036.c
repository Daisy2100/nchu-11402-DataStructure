#include <stdio.h>

int main(void) {
    // 把螢幕輸出的結果全部存進 ans3.txt 檔案裡，終端機不會印東西
    freopen("ans3.txt", "w", stdout);

    int n;
    // 不斷讀取矩陣的大小 n，當 n 為 0 的時候代表測資結束
    while (scanf("%d", &n) == 1 && n != 0) {
        
        // 準備兩個陣列，用來記錄「每一橫列 (row)」和「每一直行 (column)」的數字總和
        // 假設矩陣最大不超過 100x100，所以開大小為 100，並且全部自動初始化為 0
        int row_sums[100] = {0};
        int col_sums[100] = {0};

        // --- 階段 1：讀取矩陣資料，並隨手計算每一列、每一行的總和 ---
        for (int i = 0; i < n; i++) {       // i 代表第幾個橫列 (Row)
            for (int j = 0; j < n; j++) {   // j 代表第幾個直行 (Col)
                int value;
                scanf("%d", &value);
                
                // 把讀到的數字，加到它對應的「橫列」與「直行」的總和計數器裡面
                row_sums[i] += value;
                col_sums[j] += value;
            }
        }

        // 準備變數來記錄「總和是奇數」的數量
        int odd_rows = 0; // 有幾個橫列的總和是奇數
        int odd_cols = 0; // 有幾個直行的總和是奇數
        
        // 準備變數來記錄「出問題的座標位置」（如果有找到的話）
        int odd_r_idx = -1;
        int odd_c_idx = -1;

        // --- 階段 2：檢查算出來的總和，找出究竟是哪些橫列與直行出了狀況 ---
        for (int i = 0; i < n; i++) {
            // 檢查第 i 橫列的總和是不是奇數 (取餘數 2 不等於 0)
            if (row_sums[i] % 2 != 0) {
                odd_rows++;      // 發現一個出狀況的橫列，數量 + 1
                odd_r_idx = i;   // 把這個橫列的索引記下來
            }
            // 檢查第 i 直行的總和是不是奇數
            if (col_sums[i] % 2 != 0) {
                odd_cols++;      // 發現一個出狀況的直行，數量 + 1
                odd_c_idx = i;   // 把這個直行的索引記下來
            }
        }

        // --- 階段 3：根據「出問題」的列/行數量，做出最後判決 ---
        if (odd_rows == 0 && odd_cols == 0) {
            // 情況 1：完全沒有奇數總和，大家都乖乖符合偶數的規定 -> 一切正常
            printf("OK\n");
        } else if (odd_rows == 1 && odd_cols == 1) {
            // 情況 2：剛好只有 1 個橫列和 1 個直行總和是奇數
            // 也就是說：只要把那個交叉點數字換掉 (0變1或1變0)，就能修復整個矩陣！
            // 注意：題目要求的座標通常是日常生活從 1 開始算的，所以把記錄好的索引(0起跳)各 + 1
            printf("Change (%d,%d)\n", odd_r_idx + 1, odd_c_idx + 1);
        } else {
            // 情況 3：有超過 1 個以上的異常，或者不對等(例如行有錯但列沒錯)
            // 代表無法只靠「改一個數字」就修復 -> 判定為矩陣已毀損
            printf("Corrupt\n");
        }
    }
    return 0;
}
