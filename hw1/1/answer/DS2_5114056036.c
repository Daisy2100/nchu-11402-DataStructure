#include <stdio.h>

// --- 小幫手函式：用來判斷一個數字是不是「質數」 ---
int is_prime(int num) {
    // 質數必須是大於 1 的整數（2, 3, 5, 7...），所以小於 2 的絕對不可能是
    if (num < 2) return 0;
    
    // 檢查這個數字能不能被其他數字整除。
    // 小技巧：只要檢查到這個數字的「平方根」就可以了！
    // 寫作 i * i <= num，就不用呼叫慢吞吞的 sqrt() 數學函式。
    for (int i = 2; i * i <= num; i++) {
        if (num % i == 0) {
            return 0;  // 只要能被任何一個數整除，它就不是質數，回傳 0 (False)
        }
    }
    return 1;  // 能撐過上面的考驗，它就是貨真價實的質數，回傳 1 (True)
}

int main(void) {
    // 把螢幕輸出的結果全部存進 ans2.txt 檔案裡，終端機不會印東西
    freopen("ans2.txt", "w", stdout);

    int n;
    // 不斷讀取測資，直到測資給你 0 的時候，就乖乖結束程式
    while (scanf("%d", &n) == 1 && n != 0) {
        int count = 0; // 用來記錄「有幾對質數組合」的計數器
        
        // --- 核心戰術 ---
        // 把數字 n 拆成兩半（例如：i 和 n-i）。
        // 為了避免重複計算（像是 3+7 算一次，7+3 又算一次），
        // 迴圈只要跑到 n 的「一半（n / 2）」就可以了！
        for (int i = 2; i <= n / 2; i++) {
            // 利用上面的小幫手，檢查兩個拆開的數字是不是「都是」質數
            if (is_prime(i) && is_prime(n - i)) {
                count++; // 如果兩邊都是質數，我們就成功找到一種組合啦！
            }
        }
        
        // 印出這組 n 總共有多少對質數組合
        printf("%d\n", count);
        fflush(stdout);
    }
    return 0;
}
